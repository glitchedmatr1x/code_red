#!/usr/bin/env python3
"""
Code RED Faction War WorldResourceBridge builder.

This is intentionally a no-EXE/no-SC-CL helper. It rebuilds the loose
resource-side bridge files from factionwar_nodes.json and factionwar_factions.json,
and can safely patch an exported game_main.tr text file by adding the Code RED AI
include and world program call.

Usage:
  python WorldResourceBridge/tools/codered_bridge_build.py --root . --build
  python WorldResourceBridge/tools/codered_bridge_build.py --patch-game-main INPUT.tr OUTPUT.tr
"""
