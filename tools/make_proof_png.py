def png_write(path, w, h, pixels):
    raw = b''.join(b'\x00' + bytes(row) for row in pixels)
    def chunk(t, d):
        return struct.pack('>I', len(d)) + t + d + struct.pack('>I', zlib.crc32(t + d) & 0xffffffff)
    data = b'\x89PNG\r\n\x1a\n' + chunk(b'IHDR', struct.pack('>IIBBBBB', w, h, 8, 2, 0, 0, 0)) + chunk(b'IDAT', zlib.compress(raw, 9)) + chunk(b'IEND', b'')
    Path(path).write_bytes(data)

def draw_text(pix, x, y, text, color=(245,225,225), scale=2):
    for ch in text.upper():
        glyph = FONT.get(ch, FONT[' '])
        for gy, row in enumerate(glyph):
            for gx in range(5):
                if row & (1 << (4-gx)):
                    for sy in range(scale):
                        for sx in range(scale):
                            yy = y + gy*scale + sy; xx = x + gx*scale + sx
                            if 0 <= yy < len(pix) and 0 <= xx < len(pix[0])//3:
                                idx = xx*3; pix[yy][idx:idx+3] = bytes(color)
        x += 6*scale

